<script setup lang="ts">
import type { DestructureArray } from '#shared/types'

const props = defineProps<{
	id: string
	budgetPlan: string
}>()

const { data } = useFetch(`/api/budgetPlans/${props.budgetPlan}`)
const items = computed(() => data.value?.items ?? [])

const model = defineModel<DestructureArray<typeof items.value> | null>({
	required: true,
})

const selectModel = computed({
	get: () => model.value?.id ?? '',
	set: (v) => {
		if(v === '') {
			model.value = null
			return
		}
		model.value = items.value.find(({ id }) => id === v) ?? null
	},
})
</script>

<template lang="pug">
.kern-form-input__select-wrapper
	select.kern-form-input__select(
		:id="props.id"
		v-model="selectModel"
	)
		option(
			value=""
		) - Bitte w√§hlen -
		option(
			v-for="item of items"
			:key="item.id"
			:value="item.id"
		)
			template(v-if="item.ord")
				| {{ item.ord }} -
			|
			| {{ item.title }}
</template>
